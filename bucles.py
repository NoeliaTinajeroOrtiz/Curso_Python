#Bloque FOR
print("Números del 1 al 5 multiplicados por 2 con un bucle for: ")
for numero in range( 1, 6 ):
    print( numero * 2 )

#Bloque WHILE
print("\nNúmeros del 1 al 5 multiplicados por 2 con un bucle while:")
contador = 1
while contador <= 5:
    print(contador * 2 )
    contador += 1